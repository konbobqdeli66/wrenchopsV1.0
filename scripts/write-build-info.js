// Generates frontend/src/buildInfo.js using Git metadata.
// Intended to run before the frontend build on the server.

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const safeExec = (cmd) => {
  try {
    return String(execSync(cmd, { stdio: ['ignore', 'pipe', 'ignore'] }) || '').trim();
  } catch {
    return '';
  }
};

const formatBgDate = (yyyyMmDd) => {
  const m = String(yyyyMmDd || '').trim().match(/^(\d{4})-(\d{2})-(\d{2})$/);
  if (!m) return String(yyyyMmDd || '').trim() || '';
  return `${m[3]}.${m[2]}.${m[1]}`;
};

const commitCountRaw = safeExec('git rev-list --count HEAD');
const commitCount = Number(commitCountRaw) || 0;
const commitDateIso = safeExec('git log -1 --format=%cs');
const commitDateBg = formatBgDate(commitDateIso);

const outPath = path.join(__dirname, '..', 'frontend', 'src', 'buildInfo.js');
const content = `// AUTO-GENERATED by scripts/write-build-info.js
// Do not edit manually.

export const APP_COMMIT_COUNT = ${commitCount};
export const APP_COMMIT_DATE = ${JSON.stringify(commitDateBg)};
export const APP_VERSION_LABEL = \`WrenchOps V.1.\${APP_COMMIT_COUNT} (\${APP_COMMIT_DATE})\`;
`;

fs.writeFileSync(outPath, content, 'utf8');
console.log(`[buildInfo] wrote ${outPath} (count=${commitCount}, date=${commitDateBg || 'n/a'})`);

